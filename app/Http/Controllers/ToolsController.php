    public function emailViewer(Request $request){
        $email_sent = null;
        if($request->has('id')){
            $email_sent = EmailSent::findOrFail(array_get($request, 'id'));
        }

        $emails = EmailSent::orderBy('id', 'desc')->paginate();
        $data = [
            'emails' => $emails,
            'email_sent' => $email_sent
        ];
        return view('tools.emails.index', $data);
    }
